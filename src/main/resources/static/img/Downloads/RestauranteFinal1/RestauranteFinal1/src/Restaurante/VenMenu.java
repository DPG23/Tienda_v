    private void GuiBottonCargarOrdenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuiBottonCargarOrdenActionPerformed
        FacturaBebidas FacturaCargada = null;

        JFileChooser ventana = new JFileChooser(); // Asi Creo mi ventana
        int opcion = ventana.showOpenDialog(null); //Para Cargar el archivo

        if (opcion == JFileChooser.APPROVE_OPTION) {
            try {
                File Restaurante = ventana.getSelectedFile(); //Lo que me regresa como la cerre
                FileInputStream canal = new FileInputStream(Restaurante);
                ObjectInput mood = new ObjectInputStream(canal); //Mode de escritura de objetos en este leo

                FacturaBebidas FacturaB = (FacturaBebidas) mood.readObject();

                mood.close();
                GuiTArDetalle.setText("Factura compra Bebidas");
                GuiTArDetalle.append("\n-Nombre:\n " + "----" + FacturaB.nombre);
                GuiTArDetalle.append("\n-Tipo bebida:\n " + "----" + FacturaB.TipoBebida);
                GuiTArDetalle.append("\n-Tamaño:\n " + "----" + FacturaB.Tamaño);
                GuiTArDetalle.append("\n-Medida:\n " + "----" + FacturaB.medida);
                GuiTArDetalle.append("\n-Precio:\n " + "----" + FacturaB.total);
                GuiTArDetalle.append("\n-Comida seleccionada" + "----" + FacturaB.comida);
            } catch (IOException | ClassNotFoundException e) { //Exepciones de que se fue la conexion etc;
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_GuiBottonCargarOrdenActionPerformed

