    private void guiOrdenarAlmuerzoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guiOrdenarAlmuerzoActionPerformed
        //System.out.println(GuiTxtNombre.getText());
        boolean validacion = false;
        String nombre = GuiTxtNombre.getText(); //lo guardo en una variable
        int medida = GuiSliderMedida.getValue();// Solo falta Imprimirlo
        // System.out.println(medida);
        String Tamaño = "";

        if (GuiRadioButtonGrande.isSelected()) { //Decidir  EL BOTTON RADIO
            Tamaño = GuiRadioButtonGrande.getText();
        } else if (GuiRadioButtonMediano.isSelected()) {
            Tamaño = GuiRadioButtonMediano.getText(); //El texto que tenga medidano
        } else if (GuiRadioButtonPequeño.isSelected()) {
            Tamaño = GuiRadioButtonPequeño.getText();

        }

        //Seleccion de comidas por checks
        String comida="";

        if(guiEnchiladas.isSelected()){
            comida += guiEnchiladas.getText()+", ";
            validacion = true;
        }
        //verifica de uno en uno si esta seleccionada agrega el texto para mostrar en la orden
        if(guiHuevosPanquequeTocinetas.isSelected()){
            comida += guiHuevosPanquequeTocinetas.getText()+", ";
            validacion = true;
        }
        if(guiHuevosSalchichaTostada.isSelected()){
            comida += guiHuevosSalchichaTostada.getText()+", ";
            validacion = true;
        }
        if(guiHuevosTostada.isSelected()){
            comida += guiHuevosTostada.getText()+", ";
            validacion = true;
        }
        if(guiPinto.isSelected()){
            comida += guiPinto.getText()+", ";
            validacion = true;
        }

        if(guiArrozPollo.isSelected()){
            comida += guiArrozPollo.getText()+", ";
            validacion = true;
        }
        if(guiLasana.isSelected()){
            comida += guiLasana.getText()+", ";
            validacion = true;
        }
        if(guiPezcado.isSelected()){
            comida += guiPezcado.getText()+", ";
            validacion = true;
        }
        if(guihamburgesa.isSelected()){
            comida += guihamburgesa.getText()+", ";
            validacion = true;
        }
        if(guiPollo.isSelected()){
            comida += guiPollo.getName()+", ";
            validacion = true;
        }

        //JOptionPane.showMessageDialog(null, Tamaño);
        // System.out.println(Tamaño);
        if((GuiTxtNombre.getText().length() != 0)&&(validacion!=false)){

            int tipoBebidaIndice = GuiComboxBebida.getSelectedIndex();
            String TipoBebida = (String) GuiComboxBebida.getSelectedItem();
            //Para que lo combierta en un String y asi optenga el Texto
            System.out.println(TipoBebida);
            FacturaBebidas compra = new FacturaBebidas(nombre, medida, Tamaño, tipoBebidaIndice,
                TipoBebida,comida);

            JFileChooser ventana = new JFileChooser(); // Asi Creo mi ventana
            int opcion = ventana.showSaveDialog(null); //Para guadar el archivo

            if (opcion == JFileChooser.APPROVE_OPTION) {
                try {
                    File Restaurante = ventana.getSelectedFile(); //Lo que me regresa como la cerre
                    FileOutputStream canal = new FileOutputStream(Restaurante);
                    ObjectOutput mood = new ObjectOutputStream(canal); //Modo de escritura de objetos

                    mood.writeObject(compra);
                    mood.close();

                } catch (IOException e) { //Exepciones de que se fue la conexion etc;
                    System.out.println(e);
                }
            }
        }else{
            NoNombre ventanaError = new NoNombre();
            ventanaError.setVisible(true);
            setLocationRelativeTo(this);
        }
    }//GEN-LAST:event_guiOrdenarAlmuerzoActionPerformed

